// return sum of two numbers
const add = (num1, num2) => {
  return num1 + num2;
}

// return difference of two numbers
const sub = (num1, num2) => {
  return num1 - num2;
}

// return the multiplication of two numbers
const multiply = (num1, num2) => {
  return num1 * num2;
}

// division of two numbers
const divide = (num1, num2) => {
  return num1 / num2;
}

